import SwiftUI

// Provides predefined gradient definitions for consistent usage across the design system.
public extension Gradient {

  static let brown = Gradient(colors: [.gradientBrownFirst, .gradientBrownSecond])

  static let red = Gradient(colors: [.gradientRedFirst, .gradientRedSecond])

  static let yellow = Gradient(colors: [.gradientYellowFirst, .gradientYellowSecond])

  static let orange = Gradient(colors: [.gradientOrangeFirst, .gradientOrangeSecond])

  static let green = Gradient(colors: [.gradientGreenFirst, .gradientGreenSecond])

  static let blue = Gradient(colors: [.gradientBlueFirst, .gradientBlueSecond])

  static let purple = Gradient(colors: [.gradientPurpleFirst, .gradientPurpleSecond])

  static let teal = Gradient(colors: [.gradientTealFirst, .gradientTealSecond])
}
