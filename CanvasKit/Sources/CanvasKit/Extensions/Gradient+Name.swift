  static let ckRed = Gradient(colors: [.ckGradientRedFirst, .ckGradientRedSecond])

  static let ckYellow = Gradient(colors: [.ckGradientYellowFirst, .ckGradientYellowSecond])

  static let ckOrange = Gradient(colors: [.ckGradientOrangeFirst, .ckGradientOrangeSecond])

  static let ckGreen = Gradient(colors: [.ckGradientGreenFirst, .ckGradientGreenSecond])

  static let ckBlue = Gradient(colors: [.ckGradientBlueFirst, .ckGradientBlueSecond])

  static let ckPurple = Gradient(colors: [.ckGradientPurpleFirst, .ckGradientPurpleSecond])

  static let ckTeal = Gradient(colors: [.ckGradientTealFirst, .ckGradientTealSecond])
}
